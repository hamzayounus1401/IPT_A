package com.example.ipt_aa.Model;

public class CourseGrade {
    String name;
    String code;
    String Credit;
    String type;
    String grade;
    String points;

    public CourseGrade(String name, String code, String credit, String type, String grade, String points) {
        this.name = name;
        this.code = code;
        Credit = credit;
        this.type = type;
        this.grade = grade;
        this.points = points;
    }

    public CourseGrade() {
    }
}
